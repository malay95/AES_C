#include <stdio.h>
#include "AES.h"

int main(void)
{
     key_t key128, key192, key256;
     block_t message, cipher, reverse1, reverse2, reverse3;
     int i,j;
     
     uint8_t standard_message[16] = {0x00,0x11,0x22,0x33,0x44,0x55,0x66,0x77,0x88,0x99,0xaa,0xbb,0xcc,0xdd,0xee,0xff};
     
     uint8_t standard_key_128[16] = {0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f};
     uint8_t standard_key_192[24] = {0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f,0x10,0x11,0x12,0x13,0x14,0x15,0x16,0x17};
     uint8_t standard_key_256[32] = {0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f,0x10,0x11,0x12,0x13,0x14,0x15,0x16,0x17,0x18,0x19,0x1a,0x1b,0x1c,0x1d,0x1e,0x1f};
     uint8_t r_val[4];

     init_key(&key128, KEY128);
     init_key(&key192, KEY192);
     init_key(&key256, KEY256);
     init_block(&message);
     init_block(&cipher);
     init_block(&reverse1);
     init_block(&reverse2);
     init_block(&reverse3);


     /* Copy standard_key to key_t */
     for (i=0;i<16;i++) {
          key128.key.key128.key[i] = standard_key_128[i];
          message.array[i] = standard_message[i];
     }
     for (i=0;i<24;i++) {
          key192.key.key192.key[i] = standard_key_192[i];
     }
     for (i=0;i<32;i++) {
          key256.key.key256.key[i] = standard_key_256[i];
     }
     

     Cipher(&message, &cipher, &key128);
     InverseCipher(&cipher, &reverse1, &key128);
     Cipher(&message, &cipher, &key192);
     InverseCipher(&cipher, &reverse2, &key192);
     Cipher(&message, &cipher, &key256);
     InverseCipher(&cipher, &reverse3, &key256);

     return 0;
}
